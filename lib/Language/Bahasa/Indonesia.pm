=head1 NAMA
		Language::Bahasa::Indonesia - Write Perl program in Bahasa Indonesia

=head1 SYNOPSIS
		use Language::Bahasa::Indonesia qw(pemula);
		cetak "Apa kabar";		# print "Apa kabar";
		die "proses dihentikan";	# mati;
		
		# or
		
		use Language::Bahasa::Indonesia qw(pujangga);
		buka EMAIL di "| /usr/lib/sendmail -t" atau mati "tidak bisa buka file\n".	# print "Apa kabar";
		
		bunuh aplikasi_sekarang.	# kill ('SIGKILL', $$);
		

=head1 DESKRIPSI
		This module is more related to Lingua::Romana::Perligata but the writing
		system is totaly different.
		
		`pemula` is just to simplify the language from English-based to Indonesian.
		
		`pujangga` is the another variation on a theme, and completely recast the
		language and it's more like look similar to Perligata.pm

